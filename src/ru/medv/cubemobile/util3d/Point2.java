package ru.medv.cubemobile.util3d;

/**
 * A two-dimensional integer point.
 * @author darkbarker <barkdarker@gmail.com>
 */
public class Point2
{
    int x;
    int y;
    
    public Point2()
    {
    }
    
    public Point2(int x, int y)
    {
        this.x = x;
        this.y = y;
    }  
}
